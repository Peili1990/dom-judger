package org.nv.dom.domain.message;

public class OfflineMessage {
	
	/** 离线消息类型 */
	private Integer type;
	/** 离线消息数量 */
	private Integer num;
	public Integer getType() {
		return type;
	}
	public void setType(Integer type) {
		this.type = type;
	}
	public Integer getNum() {
		return num;
	}
	public void setNum(Integer num) {
		this.num = num;
	}
	
	

}
